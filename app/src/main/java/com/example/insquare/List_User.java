package com.example.insquare;

public class List_User {
    private String p_logo;
    private String p_name;
    private String p_department;
    private String p_position;
    private String p_company;
    private  String uid;

    public List_User(){}

    public String getP_logo() {
        return p_logo;
    }

    public void setP_logo(String p_logo) {
        this.p_logo = p_logo;
    }

    public String getP_name() {
        return p_name;
    }

    public void setP_name(String p_name) {
        this.p_name = p_name;
    }

    public String getP_department() {
        return p_department;
    }

    public void setP_department(String p_department) {
        this.p_department = p_department;
    }

    public String getP_position() {
        return p_position;
    }

    public void setP_position(String p_position) {
        this.p_position = p_position;
    }

    public String getP_company() {
        return p_company;
    }

    public void setP_company(String p_company) {
        this.p_company = p_company;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }
}
